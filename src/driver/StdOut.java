package driver;

/**
 * A class that handles outputs data to standard output.
 * 
 * @author Johnson Zhong
 *
 */
public class StdOut {

  /**
   * Displays text to standard output.
   * 
   * @param text The string to be output onto standard output.
   */
  public static void displayText(String text) {
    System.out.println(text);
  }
}
